acc_rf, auc_rf, prob_rf = evaluate_model("Random Forest", rf_pipe, X_test, y_test)
acc_xgb, auc_xgb, prob_xgb = evaluate_model("XGBoost (Final)", xgb_pipe_final, X_test, y_test)

